// Convert a String to a Number
let price = "200";

let priceAsInt = parseInt(price);
let priceAsNumber = Number(price);
let priceAsPlus = +price;

console.log(priceAsInt);  
console.log(priceAsNumber);  
console.log(priceAsPlus);  

// Convert a Number to a String 
let distance = 150;

let distanceStr1 = distance.toString();

let distanceStr2 = String(distance);

console.log(distanceStr1);
console.log(distanceStr2);

